package yb222ce_assigin1;

public class Print {
	public static void main(String[] args) {

		// on one line,
		System.out.println("Knowledge is power!");

		System.out.println("---------------------------------");

		// on three lines, one word on each line,
		System.out.println("Knowledge");
		System.out.println("is");
		System.out.println("power!");

		System.out.println("---------------------------------");

		// inside a rectangle made up by the characters = and |.
		System.out.println(" ======================= ");
		System.out.println("|                       |");
		System.out.println("|   Knowledge is power! |");
		System.out.println("|                       |");
		System.out.println("|                       |");
		System.out.println(" ======================= ");

	}

}
